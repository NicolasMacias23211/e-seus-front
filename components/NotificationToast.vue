<template>
  <div
    v-if="isVisible"
    :id="toastId"
    class="custom-toast flex items-start gap-4 p-5 rounded-xl mb-4 shadow-xl min-w-[350px] max-w-[450px] animate-slide-in-right relative overflow-hidden"
    :style="{ backgroundColor: config.bg }"
  >
    <div class="flex-shrink-0">
      <component
        :is="config.icon"
        class="w-7 h-7"
        :style="{ color: config.iconColor }"
      />
    </div>

    <div class="flex-1 text-white">
      <div class="font-bold text-base mb-1">{{ title }}</div>
      <div class="text-sm opacity-95">{{ message }}</div>
    </div>

    <button
      @click="close"
      class="text-white opacity-80 hover:opacity-100 transition-opacity text-xl leading-none p-0 bg-transparent border-none cursor-pointer"
      title="Cerrar"
    >
      <svg
        class="w-5 h-5"
        fill="none"
        stroke="currentColor"
        viewBox="0 0 24 24"
      >
        <path
          stroke-linecap="round"
          stroke-linejoin="round"
          stroke-width="2"
          d="M6 18L18 6M6 6l12 12"
        />
      </svg>
    </button>
    <div
      class="absolute bottom-0 left-0 h-1 bg-white/50 animate-progress"
      :style="{ animationDuration: `${duration}ms` }"
    ></div>
  </div>
</template>

<script setup lang="ts">
import { ref, onMounted, computed, h } from "vue";

export interface NotificationProps {
  type: "success" | "error" | "warning" | "info";
  title: string;
  message: string;
  duration?: number;
}

const props = withDefaults(defineProps<NotificationProps>(), {
  duration: 5000,
});

const emit = defineEmits<{
  (e: "close"): void;
}>();

const isVisible = ref(false);
const toastId = ref(`toast-${Date.now()}`);

const notificationConfig = {
  success: {
    bg: "#075e54",
    icon: h(
      "svg",
      { class: "w-7 h-7", fill: "currentColor", viewBox: "0 0 20 20" },
      [
        h("path", {
          "fill-rule": "evenodd",
          d: "M10 18a8 8 0 100-16 8 8 0 000 16zm3.707-9.293a1 1 0 00-1.414-1.414L9 10.586 7.707 9.293a1 1 0 00-1.414 1.414l2 2a1 1 0 001.414 0l4-4z",
          "clip-rule": "evenodd",
        }),
      ]
    ),
    iconColor: "#ffffff",
  },
  error: {
    bg: "#e8240e",
    icon: h(
      "svg",
      { class: "w-7 h-7", fill: "currentColor", viewBox: "0 0 20 20" },
      [
        h("path", {
          "fill-rule": "evenodd",
          d: "M10 18a8 8 0 100-16 8 8 0 000 16zM8.707 7.293a1 1 0 00-1.414 1.414L8.586 10l-1.293 1.293a1 1 0 101.414 1.414L10 11.414l1.293 1.293a1 1 0 001.414-1.414L11.414 10l1.293-1.293a1 1 0 00-1.414-1.414L10 8.586 8.707 7.293z",
          "clip-rule": "evenodd",
        }),
      ]
    ),
    iconColor: "#ffffff",
  },
  warning: {
    bg: "#ffbe00",
    icon: h(
      "svg",
      { class: "w-7 h-7", fill: "currentColor", viewBox: "0 0 20 20" },
      [
        h("path", {
          "fill-rule": "evenodd",
          d: "M8.257 3.099c.765-1.36 2.722-1.36 3.486 0l5.58 9.92c.75 1.334-.213 2.98-1.742 2.98H4.42c-1.53 0-2.493-1.646-1.743-2.98l5.58-9.92zM11 13a1 1 0 11-2 0 1 1 0 012 0zm-1-8a1 1 0 00-1 1v3a1 1 0 002 0V6a1 1 0 00-1-1z",
          "clip-rule": "evenodd",
        }),
      ]
    ),
    iconColor: "#001950",
  },
  info: {
    bg: "#00c8ff",
    icon: h(
      "svg",
      { class: "w-7 h-7", fill: "currentColor", viewBox: "0 0 20 20" },
      [
        h("path", {
          "fill-rule": "evenodd",
          d: "M18 10a8 8 0 11-16 0 8 8 0 0116 0zm-7-4a1 1 0 11-2 0 1 1 0 012 0zM9 9a1 1 0 000 2v3a1 1 0 001 1h1a1 1 0 100-2v-3a1 1 0 00-1-1H9z",
          "clip-rule": "evenodd",
        }),
      ]
    ),
    iconColor: "#001950",
  },
};

const config = computed(
  () => notificationConfig[props.type] || notificationConfig.info
);

const close = () => {
  isVisible.value = false;
  setTimeout(() => {
    emit("close");
  }, 300);
};

onMounted(() => {
  isVisible.value = true;

  if (props.duration > 0) {
    setTimeout(() => {
      close();
    }, props.duration);
  }
});
</script>

<style scoped>
@keyframes slideInRight {
  from {
    transform: translateX(100%);
    opacity: 0;
  }
  to {
    transform: translateX(0);
    opacity: 1;
  }
}

@keyframes progressBar {
  from {
    width: 100%;
  }
  to {
    width: 0%;
  }
}

.animate-slide-in-right {
  animation: slideInRight 0.4s ease-out;
}

.animate-progress {
  animation: progressBar linear;
}
</style>
